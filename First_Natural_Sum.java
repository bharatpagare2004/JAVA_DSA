// sum of first n natural number....
// n=4
public class First_Natural_Sum {

    public static void main(String args[])
    {
        int n = 4;
        int sum = 0;
        for(int i = 1; i<=n;i++)
        {
            sum = sum +i;
            System.out.println(sum);
        }
        
    }
    
}
